    private void botonagregar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonagregar1ActionPerformed
         // BOTON ACEPTAR
         //GUARDA LOS NUEVOS VALORES
        dpto.setId_dpto(Integer.parseInt(txtId.getText()));
        dpto.setNombre(txtNombre.getText());
        dpto.setUbicacion(txtUbicacion.getText());
        dpto.setTelefono(txtTelefono.getText());
        dpto.setMail(txtEmail.getText());
        //MANDA A LLAMAR AL METODO MODIFICAR DE LA CLASE DEPARTAMENTO
        dpto.modificar();   
    }//GEN-LAST:event_botonagregar1ActionPerformed

      public void modificar(){
        //PONE LOS DATOS DEL REGISTRO EN LA INTERFACE
        txtId.setText(String.valueOf(Estatica.getD().getId_dpto()));
        txtNombre.setText(String.valueOf(Estatica.getD().getNombre()));
        txtUbicacion.setText(String.valueOf(Estatica.getD().getUbicacion()));
        txtTelefono.setText(String.valueOf(Estatica.getD().getTelefono()));
        txtEmail.setText(String.valueOf(Estatica.getD().getMail()));
        
        
        
     }
