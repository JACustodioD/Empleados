    //METODO PARA QUE APAREZCAN LAS SECCIONES EN EL CHECKBOX
     public void seccion (){
        
            String sql = "SELECT id_sec from seccion";
            con = new Conectar();
         try{   
            Statement st;
            st = con.getConnection().createStatement();
            ResultSet rs = st.executeQuery(sql);
            comIdSec.removeAllItems();
            while(rs.next()){
                comIdSec.addItem(rs.getString(1));
            }
        } catch (SQLException ex) {
            Logger.getLogger(JProyecto.class.getName()).log(Level.SEVERE, null, ex);
        }
          
          
               
           }
