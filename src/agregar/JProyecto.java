    private void btnagregar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnagregar1ActionPerformed
        // BOTON AGREGAR
        
        //GUARDA LOS DATOS EN LOS ATRIBUTOS DE LA CLASE
        p.setClave_proyecto(Integer.parseInt(txtClave.getText()));
        
        Fecha date = new Fecha();
        date.setDia(comDia.getSelectedItem().toString());
        date.setMes(comMes.getSelectedItem().toString());
        date.setAño(comAño.getSelectedItem().toString());
        
        Fecha date2 = new Fecha();
        date2.setDia(comDia2.getSelectedItem().toString());
        date2.setMes(comMes2.getSelectedItem().toString());
        date2.setAño(comAño2.getSelectedItem().toString());
        
        p.setFecha_i(date);
        p.setFecha_f(date2);
        p.setDescripcion(txtDescripcion.getText());
        p.setId_sec(Integer.parseInt(comIdSec.getSelectedItem().toString()));
        
        //MANDA A LLAMR EL METODO AGREGAR DE LA CLASE PROYECTO
        p.agregar();
        
        //LIMPIA EL CAMPO DESCRIPCION
        txtDescripcion.setText(null);
    }//GEN-LAST:event_btnagregar1ActionPerformed

